'use client';

import { useEffect, useRef } from 'react';

export function Character() {
  const preRef = useRef<HTMLPreElement>(null);
  const frameRef = useRef<number | null>(null);

  useEffect(() => {
    if (!preRef.current) return;

    // 纯粹的回归：完全使用你最初提供的 txt 原文，不增删任何一个空格
    // 它本身就是一个完美斜向上的香蕉轮廓
    const originalAscii = `                                                                                                                                                                                                              
                                                                                                                    .:-=+-.                           
                                                                                                                  .+%%%###%%-                          
                                                                                                                .*%##%%%%##=                          
                                                                                                                .##+###+-+*.                          
                                                                                                                .+#=:=+--#=                           
                                                                                                                .+#*:-+=-#-                           
                                                                                                                :#+*--+=-*=                           
                                                                                                                .*++*-:+=-=#.                          
                                                                                                              =#=-*+::+=--+*.                         
                                                        .--.                                                 :*+-:+*-::+=---=#:                        
                                                      -*%%%+**+-.                                          :*+-::+#-:::+=----=#-                       
                                                    -*%%%=:::-=+*+-.                                   .-**-:::+#=::::+=-----=#:                      
                                                    :**=-*+::::::-=**+:                              :+*+-:::-*#=::::-+=------++                      
                                                      -*---=+=::::::::-+**+-.                     .-+*+=-::::=#%=:::::=+-------=*:                     
                                                      +*----=+=-:::::::::-=+**+=-:..     ..:-=+**+=-::::::=+*#-::::::++--------*-                     
                                                      .*+-----=++-::::::::::::--=++*******++==-:::::::::-++**-::::::=+---------*=                     
                                                        .*+-------++=-::::::::::::::::::::::::::::::::-=+=+#+-::::::-+=---------*-                     
                                                        .**---------+++=-:::::::::::::::::::::::::-=++==**-:::::::-++---------=*.                     
                                                          =#=----------=+++=--:::::::::::::::-==+++--=**=::::::::-++----------*+                      
                                                            :+*--------------=+++++++++++++++++=----+*+-:::::::::-+=----------=*.                      
                                                            .:**=------------------------------=+**=-::::::::::=*=-----------#-                       
                                                      .=#%**++=+##+=----------------------==+**+-::::::::::::-*+------------#=                        
                                                      .*%#%+::::-==++**#***+++++==++++*****+=-::::::::::::::-++-------------#=                         
                                                      =#%%#+--:::::::::::::::--------::::::::::::::::::::-=*+-------------=#=                          
                                                      -##*--=*+=-:::::::::::::::::::::::::::::::::::::-=++=--------------=#:                           
                                                      -*=------+*+=--::::::::::::::::::::::::::::--=**=----------------*+.                            
                                                        :#=---------=+*+=----::::::::::::::----+**+-------------------=*-.                             
                                                        .*+---------------=+*****+++++****+=-----------------------=*+.                               
                                                          =#=----------------------------------------------------=*+:                                 
                                                            .**=------------------------------------------------+#+.                                   
                                                              .+*=-------------------------------------------=##:                                      
                                                                .-**=-------------------------------------+#*:                                         
                                                                    :*%*=----------------------------=*##=.                                            
                                                                        .-*###+=--------------=+#%%*=.                                                 
                                                                            ...:-==++++++==-:...                                                      
                                                                                                                                                
`;

    const lines = originalAscii.split('\n');
    let frame = 0;

    function render() {
      frame += 1;
      const time = frame * 0.05;

      const newOutput = lines.map((line, y) => {
        let animatedLine = '';

        for (let x = 0; x < line.length; x++) {
          const char = line[x];

          if (char === ' ') {
            animatedLine += ' ';
            continue;
          }

          // 【核心修复】单束对角线雷达扫描波
          // 摒弃了会产生多个波峰的 Math.sin，改用绝对距离计算
          // 周期控制：让光束从左上角 (-50) 扫描到右下角 (250)
          const cycle = (time * 18) % 300 - 50;

          // 计算当前字符距离扫描线的距离 (沿对角线 x + y*1.5 的方向)
          const dist = Math.abs(x + y * 1.5 - cycle);

          if (dist < 4) {
            // 光束正中心：极强的高光白色，确保香蕉瞬间清晰可见
            animatedLine += `<span style="color: #FFFFFF; text-shadow: 0 0 10px #FFFFFF, 0 0 20px #F2C94C;">${char}</span>`;
          } else if (dist < 12) {
            // 光束边缘：宽广的金色晕染，照亮整个主体
            animatedLine += `<span style="color: #FDE68A; text-shadow: 0 0 6px #F2C94C;">${char}</span>`;
          } else {
            // 未被光束扫到的暗部：保持正常的金色
            animatedLine += char;
          }
        }

        return animatedLine;
      });

      if (preRef.current) {
        preRef.current.innerHTML = newOutput.join('\n');
      }

      frameRef.current = requestAnimationFrame(render);
    }

    render();

    return () => {
      if (frameRef.current) {
        cancelAnimationFrame(frameRef.current);
      }
    };
  }, []);

  return (
    <div className="flex items-center justify-center w-full h-full">
      <pre
        ref={preRef}
        className="font-mono text-[0.4rem] md:text-[0.48rem] lg:text-[0.52rem] leading-[0.5] md:leading-[0.55] lg:leading-[0.58] select-none tracking-[-0.05em]"
        style={{
          color: '#F2C94C',
          textShadow: '0 0 15px rgba(242, 201, 76, 0.4)', // 稍微降低了全局底光的抢戏感，让扫描线更突出
        }}
      />
    </div>
  );
}